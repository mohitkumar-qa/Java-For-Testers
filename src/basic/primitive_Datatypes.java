package basic;

public class primitive_Datatypes {
	public static void main(String[] args) {
		
	
	byte b1 = 123;
	short sh1 = 7654;
	int num1 = 456789;
	long l1 = 3456789;
	float f1 = 12.5678f;
	double d1 = 234.456789d;
	char ch1 = 'A';
	boolean b2 = true;
	 // not primitive but we use in java 
	String str1 = "Primitve Data Types";

	System.out.println("byte	: "+b1 +"\nshort	: "+sh1+""
			+ "\nint	: "+num1+"\nlong	: "+l1+"\nfloat	: "+f1
			+"\ndouble	: "+d1+"\nchar	: "+ch1
			+"\nboolean	: "+b2+"\nSTRING	: "+str1);
	
	//  \n used to print next line 
}
	
}
