package stringMethods;

public class Starts_EndsWith {
	public static void main(String[] args) {
		
	//Checks how a String starts or ends
		
		String name = "Mohit Kumar";
		System.out.println(name.startsWith("Moh"));
		// you can use single letters or multiple letters
		
		System.out.println(name.endsWith("ar"));
		//True
	}

}
