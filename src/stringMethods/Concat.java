package stringMethods;

public class Concat {

	// use to add Strings
	public static void main(String[] args) {
		
		String name = "Mohit";
		String lastName = "Kumar";
		System.out.println("Concatination : "+name.concat(" "+lastName));
		
		// it will add both the string but it will also add space as well if there is spaces in the strings 
	}
	
}
